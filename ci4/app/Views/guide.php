    <h1 class="mt-3">Submission Guidelines</h1>
    <h3>400 Words is open for submissions year round</h3>
    <p>Thank you for your interest in 400 Words! We are an experiment of sorts – collaboratively creating a record of life stories, and stories lived, condensed into a maximum of 400 words.</p>
    <h3>Our Mission Statement:</h3>
    <p>400 Words strives to present unique, anonymous snapshots of significant moments in people’s lives – these can wholly-contained stories or meaningful moments. The only rules we have are that they must be true stories, with the acknowledgement that over a lifetime truth can sometimes develop a bit of subjectivity – which is to say that memories can be imperfect, and sometimes the better for it.</p>
    <p>The following submission guidelines apply:</p>
    <ul>
        <li>All works must be titled.</li>
        <li>No real persons may be identified by name in a submission, with the exception of already known public figures.</li>
        <li>Profanity is OK, explicit content is OK, however both are subject to limitations at our editorial board’s discretion.</li>
        <li>We do not accept essays, opinion pieces, works of fiction, art, photography, etc.</li>
        <li>All work submitted is subject to editorial review and revision.</li>
    </ul>
    <p>That’s it; we appreciate your interest in 400 Words and can’t wait to read about you!</p>
    <p>Many thanks, <br>
    The 400 Words Editorial Staff
